<script>
export default {
  name: 'App',
  data () {
    return {
      api_key: '8f8d0b8fd354a2c5dbe22c668af800ba',
      base_url: 'http://api.openweathermap.org/data/2.5/',
      query: '',
      weather: {}
    }
  },

  methods: {
    async fetchWeather (e) {
      if(e.key == 'Enter') {
        let response = await fetch(`${this.base_url}weather?q=${this.query}&units=metric&APPID=${this.api_key}`);
        let json = await response.json();
        // check the data gotten
        console.log(json);
        // pass the object as weather properties
        this.weather = await json;
      }
    }
  }
}
